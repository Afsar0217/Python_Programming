#Numpy Data Types
import numpy as np
#Integer Data Types
a=np.array([1,2,3,4,5])
print(a.dtype)
#Floating Point Data Types
b=np.array([1.4,2.7,3.44,4.0,5.1])
print(b.dtype)
#Boolean Data Types
c=np.array([(10<20)])
print(c.dtype)
#Complex Data Types
d=np.array([1+6j,2+3j,3+7j])
print(d.dtype)
#String Data Types
e=np.array(['Apple','Ball','Cat'])
print(e.dtype)
